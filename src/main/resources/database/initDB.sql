  MODEL_NAME VARCHAR NOT NULL,
  OPERATING_SYSTEM VARCHAR,
  FORM_FACTOR VARCHAR,
  Width VARCHAR,
  Height VARCHAR,
  Depth VARCHAR,
  WEIGHT VARCHAR,
  BODY_MATERIAL VARCHAR,
  BODY_COLOR VARCHAR,
  WATER_PROOF VARCHAR,
  DUST_PROOF VARCHAR,
  CRASH_WORTHINESS VARCHAR,
  PACKAGING VARCHAR,
  SOC VARCHAR,
  CPU_NUMBER_OF_CORES VARCHAR,
  CPU_ARCHITECTURE VARCHAR,
  CPU_MAX_FREQUENCY_PER_CORE VARCHAR,
  CPU_INSTRUCTION VARCHAR,
  CPU_MANUFACTURING_METHOD VARCHAR,
  GRAPHICAL_CONTROLLER_NAME VARCHAR,
  GRAPHICAL_CONTROLLER_GPU_CLOCK VARCHAR,
  RAM_SIZE VARCHAR,
  RAM_TYPE VARCHAR,
  RAM_CLOCK VARCHAR,
  ROM VARCHAR,
  SOUND_CONTROLLER VARCHAR,
  BATTERY VARCHAR,
  GPS VARCHAR,
  FM_RECEIVER VARCHAR,
  FM_TRANSMITTER VARCHAR,
  WIRELESS_CHARGER VARCHAR,
  ANTENNA VARCHAR,
  VIBRATION VARCHAR,
  LED_NOTIFICATION VARCHAR,
  SPEAKER VARCHAR,
  DISPLAY_DIAGONAL VARCHAR,
  DISPLAY_RESOLUTION VARCHAR,
  DISPLAY_TYPE VARCHAR,
  DISPLAY_RATIO VARCHAR,
  DISPLAY_COLOR_AMOUNT VARCHAR,
  DISPLAY_SENSOR_TYPE VARCHAR,
  DISPLAY_MULTITOUCH VARCHAR,
  DISPLAY_GLASS_DESCRIPTION VARCHAR,
  DISPLAY_GLARE_FILTER VARCHAR,
